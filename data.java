import java.io.*;
public class data{
	public static void main(String[] args)
	{
		 float f1 = 35e3f;
		double d1 = 12d;
		System.out.println(f1);
		System.out.println(d1);
	}
}

/*
Key Points

Size: 4 bytes (32 bits)

Precision: about 6–7 decimal digits

Must end with f or F in Java.
*/